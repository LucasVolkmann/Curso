package exemplo02;

public class Principal {

	public static void main(String[] args) {
		
		//Laço DO While
		
		int indice = 20;
		do {
			System.out.println(indice);
			indice--;
		}while(indice >= 0);
		
		/*
		 Incrementadores e Decrementadores
		 ++ incremente +1
		 ++ decrementa -1
		 +=2 incrementa dependendo do valor informado
		 -=3 decremente dependendo do valor informado
		 *=2 incremente multiplicando por um valor
		 /=5 decrementa dividindo por um valor 
		
		 */
		
		
	}

}
